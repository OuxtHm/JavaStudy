
public class 배열_5 {

	public static void main(String[] args) {
		char[] ch = {'B', 'C', 'E', 'F', 'K'};
		
		for(int i = 0; i < ch.length; i++)
		{
			System.out.print(ch[i] + " ");
		}
		System.out.println();
		
		// 검색, 페이징
		// 배열 / 컬렉션
		// 웹 출력
		for(char c : ch)
		{
			System.out.print(c + " ");
		}

	}

}
